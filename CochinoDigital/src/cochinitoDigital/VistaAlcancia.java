    private void btnRomperActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRomperActionPerformed
        int opt = JOptionPane.showConfirmDialog(rootPane, "QUIERES ROMPER LA ALCANCIA");
        if (JOptionPane.OK_OPTION == opt) {
            alcancia.RomperAlcancia();
            lblCochinito.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/cochinitoRoto.jpg")));
            btnRomper.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/UACM.jpg")));
            JOptionPane.showMessageDialog(rootPane, "EL TOTAL AHORRADO  ES DE:"+ "$" + alcancia.ContarMonedas(CochinitoDigital, total));
            total = 0;
            CochinitoDigital.clear();
            lblSaldoTotal.setText("$" + total);
            lblPropietario.setText("PROPIETARIO: ");
            lblFecha.setText("FECHA: ");
            btnAgregar.setVisible(false);

            btnNuevaAlcancia.setVisible(true);

        }
    }//GEN-LAST:event_btnRomperActionPerformed

    private void btnNuevaAlcanciaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnNuevaAlcanciaMouseClicked
        btnRomper.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/martillo1.png")));
        btnAgregar.setVisible(true);
        btnRomper.setVisible(true);
        btnNuevaAlcancia.setVisible(false);
        fechaRomper = fecha.toString();
        nombre = JOptionPane.showInputDialog("INGRESA TU NOMBRE: ");
        if (nombre.equals("")) {
            btnAgregar.setVisible(false);
            btnRomper.setVisible(false);
            btnNuevaAlcancia.setVisible(true);
        } else {
            lblCochinito.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/cochinito.jpg")));
            lblPropietario.setText("PROPIETARIO: " + nombre);
            lblFecha.setText(fechaRomper);
            alcancia.GuardarFecha(fechaRomper + "  " + nombre);
            btnAgregar.setVisible(true);
            btnRomper.setVisible(true);
            btnNuevaAlcancia.setVisible(false);
            fechaRomper = fecha.toString();
        }
    }//GEN-LAST:event_btnNuevaAlcanciaMouseClicked

    public void MonedaSeleccionada() {
        if (rbCincuentaCentavos.isSelected()) {
            JOptionPane.showMessageDialog(rootPane, "AGREGASTE UNA MONEDA DE 50 CENTAVOS");
            moneda = new Monedas(.50);
            CochinitoDigital.add(moneda);
        }
        if (rbUno.isSelected()) {
            JOptionPane.showMessageDialog(rootPane, "AGREGASTE UNA MONEDA DE 1 PESOS");
            moneda = new Monedas(1);
            CochinitoDigital.add(moneda);
        }
        if (rbDos.isSelected()) {
            JOptionPane.showMessageDialog(rootPane, "AGREGASTE UNA MONEDA DE 2 PESOS");
            moneda = new Monedas(2);
            CochinitoDigital.add(moneda);
        }
        if (rbCinco.isSelected()) {
            JOptionPane.showMessageDialog(rootPane, "AGREGASTE UNA MONEDA DE 5 PESOS");
            moneda = new Monedas(5);
            CochinitoDigital.add(moneda);
        }
        if (rbDiez.isSelected()) {
            JOptionPane.showMessageDialog(rootPane, "AGREGASTE UNA MONEDA DE 10 PESOS");
            moneda = new Monedas(10);
            CochinitoDigital.add(moneda);
        }
    }

