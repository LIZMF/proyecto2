    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        total = alcancia.ContarMonedas(alcancia.AbrirArchivo(), 0.0);
        lblSaldoTotal.setText("$" + total);
        try {
            if (alcancia.LeerFecha() == "") {

                nombre = JOptionPane.showInputDialog("POR FAVOR INGRESA TU NOMBRE: ");
                if (nombre.equals("")) {
                    btnAgregar.setVisible(false);
                    btnRomper.setVisible(false);
                    btnNuevaAlcancia.setVisible(true);
                } 
                else {
                    fechaRomper = alcancia.LeerFecha();
                    String[] stringRomper = fechaRomper.split(" ");
                    lblPropietario.setText("PROPIETARIO: " + stringRomper[1]);
                    lblFecha.setText(stringRomper[0]);
                }
            } else {
                fechaRomper = alcancia.LeerFecha();
                String[] cortarString = fechaRomper.split("  ");
                lblPropietario.setText("PROPIETARIO: " + cortarString[1]);
                lblFecha.setText(cortarString[0]);
            }
        } catch (IOException ex) {
            Logger.getLogger(VistaAlcancia.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_formWindowOpened

